package io.leonis.subra.game.data;

/**
 * The Enum TeamColor.
 *
 * This enumeration represents the possible colors a team can sport in a Small Size League
 * sequence.
 *
 * @author Jeroen de Jong
 * @author Rimon Oz
 */
public enum TeamColor {
  /**
   * Blue team color.
   */
  BLUE,
  /**
   * Yellow team color.
   */
  YELLOW,
  /**
   * None team color.
   */
  NONE
}
